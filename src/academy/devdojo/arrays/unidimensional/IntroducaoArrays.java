package academy.devdojo.arrays.unidimensional;

public class IntroducaoArrays {
    public static void main(String[] args) {
        double[] notas = new double[4];
        notas[0] = 1;
        notas[1] = 1.5;
        notas[2] = 4.5;
        notas[3] = 3.7;
        for (int i = 0; i < 4; i++) {
            System.out.println(notas[i]);
        }
    }
}
